let day = prompt("Nhập vào số bất kì");
switch (day)
{
    case "1" : alert("Đây là thứ 2");
    break;

    case "2" : alert("Đây là thứ 3");
    break;
    
    case "3" : alert("Đây là thứ 4");
    break;

    case "4" : alert("Đây là thứ 5");
    break;

    case "5" : alert("Đây là thứ 6");
    break;

    case "6" : alert("Đây là thứ 7");
    break;

    case "7" : alert("Đây là chủ nhật");
    break;
    default : alert("Vui lòng nhập lại")
}